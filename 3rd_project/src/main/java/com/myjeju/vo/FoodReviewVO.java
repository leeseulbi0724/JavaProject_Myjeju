package com.myjeju.vo;

public class FoodReviewVO {
	String fid, id, reid, f_review, f_time;
	int f_star;
	
	public String getFid() {
		return fid;
	}
	public void setFid(String fid) {
		this.fid = fid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReid() {
		return reid;
	}
	public void setReid(String reid) {
		this.reid = reid;
	}
	public String getF_review() {
		return f_review;
	}
	public void setF_review(String f_review) {
		this.f_review = f_review;
	}
	public String getF_time() {
		return f_time;
	}
	public void setF_time(String f_time) {
		this.f_time = f_time;
	}
	public int getF_star() {
		return f_star;
	}
	public void setF_star(int f_star) {
		this.f_star = f_star;
	}
}

