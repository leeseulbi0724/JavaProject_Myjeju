	public String getHid() {
		return hid;
	}
	public void setHid(String hid) {
		this.hid = hid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReid() {
		return reid;
	}
	public void setReid(String reid) {
		this.reid = reid;
	}
	public String getH_review() {
		return h_review;
	}
	public void setH_review(String h_review) {
		this.h_review = h_review;
	}
	public String getH_time() {
		return h_time;
	}
	public void setH_time(String h_time) {
		this.h_time = h_time;
	}
	public int getH_star() {
		return h_star;
	}
	public void setH_star(int h_star) {
		this.h_star = h_star;
	}
	
