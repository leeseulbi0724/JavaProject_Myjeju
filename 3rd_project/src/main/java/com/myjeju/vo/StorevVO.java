package com.myjeju.vo;

public class StorevVO {

	String id, sid, srid, sr_review, sr_time;
	int sr_star;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getSrid() {
		return srid;
	}

	public void setSrid(String srid) {
		this.srid = srid;
	}

	public String getSr_review() {
		return sr_review;
	}

	public void setSr_review(String sr_review) {
		this.sr_review = sr_review;
	}
	
	public int getSr_star() {
		return sr_star;
	}

	public void setSr_star(int sr_star) {
		this.sr_star = sr_star;
	}

	public String getSr_time() {
		return sr_time;
	}

	public void setSr_time(String sr_time) {
		this.sr_time = sr_time;
	}


	
	
}
