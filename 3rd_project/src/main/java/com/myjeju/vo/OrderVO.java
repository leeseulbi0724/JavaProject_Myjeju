package com.myjeju.vo;

public class OrderVO {
	
	String o_name, o_price, o_count, o_file, o_sfile, o_date, id;

	public String getO_name() {
		return o_name;
	}

	public void setO_name(String o_name) {
		this.o_name = o_name;
	}

	public String getO_price() {
		return o_price;
	}

	public void setO_price(String o_price) {
		this.o_price = o_price;
	}

	public String getO_count() {
		return o_count;
	}

	public void setO_count(String o_count) {
		this.o_count = o_count;
	}

	public String getO_file() {
		return o_file;
	}

	public void setO_file(String o_file) {
		this.o_file = o_file;
	}

	public String getO_sfile() {
		return o_sfile;
	}

	public void setO_sfile(String o_sfile) {
		this.o_sfile = o_sfile;
	}

	public String getO_date() {
		return o_date;
	}

	public void setO_date(String o_date) {
		this.o_date = o_date;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}


	
	

}
